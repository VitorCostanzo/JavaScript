// Math

console.log(Math.round(4.34));
console.log(Math.ceil(4.35));
console.log(Math.floor(4.8));
console.log(Math.trunc(4.567));
console.log(Math.pow(2, 10));
console.log(Math.sqrt(64));
console.log(Math.min(54, 32, 67, 12, 35));
console.log(Math.max(54, 32, 67, 12, 35));

// Random
    console.log(Math.random()); // retorna um numero aleatorio entre 0 e 1 (ponto flutuante)
    console.log(Math.trunc(Math.random()*100)); // retorna um numero inteiro entre 0 e 100
    console.log(((Math.random()*1000).toFixed(2))); // retorna um numero entre 0 e 1000, com duas casas decimais


